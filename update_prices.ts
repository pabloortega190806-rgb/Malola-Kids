async function updatePrices() {
  try {
    for (const [code, price] of Object.entries(prices)) {
      await pool.query(
        `UPDATE products 
         SET 
           original_price = $1,
           discounted_price = $1
         WHERE code = $2`,
        [price.toFixed(2), code]
      );
    }
    console.log('Prices updated successfully in DB.');

    // Update products.json
    const res = await pool.query('SELECT * FROM products');
    fs.writeFileSync('products.json', JSON.stringify(res.rows, null, 2));
    console.log('products.json updated.');

  } catch (err) {
    console.error('Error updating prices:', err);
  } finally {
    await pool.end();
  }
}

